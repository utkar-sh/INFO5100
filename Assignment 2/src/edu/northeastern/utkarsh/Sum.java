package edu.northeastern.utkarsh;

public class Sum {

    public int add(int add1, int add2){
        int sum = add1 + add2;
        return sum;
    }

    public int add(int add1, int add2, int add3){
        int sum = add1 + add2 + add3;
        return sum;
    }

    public double add(int add1, double add2){
        double sum = add1 + add2;
        return sum;
    }

    public double add(double add1, int add2){
        double sum = add1 + add2;
        return sum;
    }

    public double add(double add1, double add2){
        double sum = add1 + add2;
        return sum;
    }
}
